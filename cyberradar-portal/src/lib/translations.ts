export function t(key: keyof typeof translations.de, locale: Locale = 'de', params?: Record<string, string | number>): string {
  let text = translations[locale][key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
}


